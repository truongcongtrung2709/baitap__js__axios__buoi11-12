function User(
  id,
  account,
  name,
  password,
  email,
  image,
  type,
  language,
  description
) {
  this.id = id;
  this.account = account;
  this.name = name;
  this.password = password;
  this.email = email;
  this.image = image;
  this.type = type;
  this.language = language;
  this.description = description;
}
